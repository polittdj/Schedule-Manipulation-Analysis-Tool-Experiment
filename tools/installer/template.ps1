$ErrorActionPreference = "Stop"

function Install-Main {
# SF_INSTALLER_SMOKE=1: non-interactive CI/self-test mode — temp install root, prompts skipped,
# no shortcuts, no Ollama/Java. Used by the windows-latest smoke workflow; harmless for users.
$Smoke = ($env:SF_INSTALLER_SMOKE -eq "1")
if ($Smoke -and $env:SF_INSTALL_ROOT) { $InstallRoot = $env:SF_INSTALL_ROOT }
elseif ($Smoke) { $InstallRoot = Join-Path ([IO.Path]::GetTempPath()) "SFSmoke\ScheduleForensics" }
else { $InstallRoot = Join-Path $env:LOCALAPPDATA "ScheduleForensics" }
$VenvDir     = Join-Path $InstallRoot "venv"
$AppPort     = 8321
New-Item -ItemType Directory -Force -Path $InstallRoot | Out-Null
Start-Transcript -Path (Join-Path $InstallRoot "install.log") -Append | Out-Null

function Step([string]$msg) { Write-Host "`n==> $msg" -ForegroundColor Cyan }
function Ok([string]$msg)   { Write-Host "    [ok] $msg" -ForegroundColor Green }
function Warn2([string]$msg){ Write-Host "    [!!] $msg" -ForegroundColor Yellow }

Write-Host ""
Write-Host "Schedule Forensics installer — $TierLabel" -ForegroundColor White
Write-Host "Install location: $InstallRoot"

# --- 1. machine fit (warn-only) ------------------------------------------------------
Step "Checking machine fit for this tier"
$ramGB = [math]::Round((Get-CimInstance Win32_ComputerSystem).TotalPhysicalMemory / 1GB)
if ($ramGB -lt $MinRamGB) {
    Warn2 "This machine reports ${ramGB} GB RAM; this tier targets ${MinRamGB} GB+. The tool will run; the AI model may be slow or fail to load."
} else { Ok "${ramGB} GB RAM (tier targets ${MinRamGB} GB+)" }
if ($NeedsGpu) {
    $gpus = @(Get-CimInstance Win32_VideoController | Where-Object { $_.AdapterRAM -gt 2GB })
    if ($gpus.Count -eq 0) {
        Warn2 "No discrete GPU detected; this tier's AI model expects one. The model will run on CPU (slow) or fail to load."
    } else { Ok ("GPU detected: " + ($gpus[0].Name)) }
}

# --- 2. Python 3.11+ ------------------------------------------------------------------
Step "Checking Python 3.11+"
function Find-Python {
    foreach ($cand in @(@("py","-3.12"), @("py","-3.11"), @("python",""))) {
        $exe = $cand[0]; $flag = $cand[1]
        if (Get-Command $exe -ErrorAction SilentlyContinue) {
            try {
                if ($flag) { $v = & $exe $flag -c "import sys;print('%d.%d'%sys.version_info[:2])" 2>$null }
                else       { $v = & $exe       -c "import sys;print('%d.%d'%sys.version_info[:2])" 2>$null }
                if ($v -and ([version]$v -ge [version]"3.11")) {
                    # unary comma: PowerShell unrolls a returned 1-element array into a bare
                    # string, and then $py[0] indexes the first CHARACTER ("p" of "python")
                    if ($flag) { return ,@($exe, $flag) } else { return ,@($exe) }
                }
            } catch { }
        }
    }
    return $null
}
$py = Find-Python
if (-not $py) {
    Warn2 "Python 3.11+ not found — installing Python 3.12 via winget (official Python.org package)"
    if (-not (Get-Command winget -ErrorAction SilentlyContinue)) {
        throw "winget is not available. Install Python 3.12 manually from python.org, then re-run this installer."
    }
    winget install --id Python.Python.3.12 --exact --accept-source-agreements --accept-package-agreements --silent --scope user
    $env:Path = [Environment]::GetEnvironmentVariable("Path","Machine") + ";" + [Environment]::GetEnvironmentVariable("Path","User")
    $py = Find-Python
    if (-not $py) { throw "Python installed but not discoverable on PATH yet — open a NEW window and re-run this installer." }
}
Ok ("Python found: " + ($py -join " "))

# --- 3. the tool, into its own venv, from the wheel embedded below --------------------
Step "Installing Schedule Forensics into its own environment"
if (-not (Test-Path (Join-Path $VenvDir "Scripts\python.exe"))) {
    $py = @($py)  # defensive: never let a bare string reach the indexed invocation below
    if ($py.Count -gt 1) { & $py[0] $py[1] -m venv $VenvDir } else { & $py[0] -m venv $VenvDir }
    Ok "Created venv"
} else { Ok "venv already present (re-using)" }
$venvPy = Join-Path $VenvDir "Scripts\python.exe"

$wheelPath = Join-Path $InstallRoot "{{WHEEL_NAME}}"
$b64 = $EMBEDDED_WHEEL_B64 -replace "\s", ""
[IO.File]::WriteAllBytes($wheelPath, [Convert]::FromBase64String($b64))
& $venvPy -m pip install --quiet --upgrade pip
& $venvPy -m pip install --quiet --force-reinstall $wheelPath
& $venvPy -c "import schedule_forensics" 2>$null
if ($LASTEXITCODE -ne 0) { throw "The tool failed to import after install — see $InstallRoot\install.log" }
Ok ("Installed " + "{{WHEEL_NAME}}")
# optional HUD telemetry enhancer (CPU% / temps on Windows) — best-effort, never fatal
try { & $venvPy -m pip install --quiet psutil 2>$null } catch { }

# --- 3b. vendored MPXJ converter (native .mpp support) --------------------------------
# The wheel is pure Python; the 17 MB Java converter (tools\mpxj) rides in the repository
# next to this installer and is copied beside the venv, where the runtime's walk-up
# discovery finds it automatically (ADR-0193). Without it every .mpp import fails with
# "MPXJ runner not found".
$repoMpxj = Join-Path (Split-Path -Parent $PSScriptRoot) "tools\mpxj"
if (Test-Path (Join-Path $repoMpxj "classes\MpxjToMspdi.class")) {
    $destMpxj = Join-Path $InstallRoot "tools\mpxj"
    New-Item -ItemType Directory -Force -Path $destMpxj | Out-Null
    Copy-Item -Recurse -Force -Path (Join-Path $repoMpxj "*") -Destination $destMpxj
    Ok "MPXJ converter deployed (native .mpp import enabled)"
} else {
    Warn2 "tools\mpxj not found next to this installer — native .mpp import stays OFF"
    Warn2 "  (run the installer from the repository checkout, or set SF_MPXJ_HOME;"
    Warn2 "  .mpp files can still be opened after exporting to MSPDI XML from MS Project)"
}

# a stale 'schedule-forensics' shim from ANOTHER Python (e.g. a conda/miniforge base env)
# can shadow this install in terminals and die with ModuleNotFoundError (operator hit this,
# ADR-0192). The Desktop shortcut always launches the right venv; warn about the impostor.
$shadow = Get-Command schedule-forensics -ErrorAction SilentlyContinue
if ($shadow -and $shadow.Source -and -not $shadow.Source.StartsWith($InstallRoot)) {
    Warn2 ("Typing 'schedule-forensics' in a terminal would run " + $shadow.Source)
    Warn2 ("  — that is another Python's (possibly broken) copy, NOT this install. Use the")
    Warn2 ("  Desktop shortcut, or delete that file to clear the confusion.")
}

# --- 4. Java 17+ (optional — native .mpp only; NO ADMIN required) ---------------------
# ADR-0192: the old path ran the winget OpenJDK MSI, which pops a UAC prompt (operators on
# locked-down machines cannot approve it) and its failure was mis-reported as success. Now:
# detect existing JDKs the way the TOOL's runtime discovery does (including installs that
# are not on PATH), and on consent download Microsoft's PORTABLE ZIP into
# %LOCALAPPDATA%\Programs\Microsoft — user-writable, no elevation, no PATH edits, and
# already one of the folders the runtime java scan searches automatically.
Step "Checking Java 17+ (optional — only needed to open native .mpp files)"
function Find-JavaNoAdmin {
    if (Get-Command java -ErrorAction SilentlyContinue) {
        $jv = (& java -version 2>&1 | Select-Object -First 1) -replace '.*"(\d+).*', '$1'
        if (($jv -match '^\d+$') -and ([int]$jv -ge 17)) { return "java (on PATH)" }
    }
    $roots = @(
        (Join-Path $env:LOCALAPPDATA "Programs\Microsoft"),
        (Join-Path $env:LOCALAPPDATA "Programs\Eclipse Adoptium"),
        (Join-Path $env:LOCALAPPDATA "Programs\Java"),
        (Join-Path $env:ProgramFiles "Microsoft"),
        (Join-Path $env:ProgramFiles "Eclipse Adoptium"),
        (Join-Path $env:ProgramFiles "Java")
    )
    foreach ($root in $roots) {
        if (-not (Test-Path $root)) { continue }
        $hit = Get-ChildItem -Path $root -Directory -ErrorAction SilentlyContinue |
            Where-Object { ($_.Name -match "(?:jdk|jre)-?(\d+)") -and ([int]$Matches[1] -ge 17) } |
            Where-Object { Test-Path (Join-Path $_.FullName "bin\java.exe") } |
            Select-Object -First 1
        if ($hit) { return (Join-Path $hit.FullName "bin\java.exe") }
    }
    return $null
}
$javaWhere = Find-JavaNoAdmin
if ($javaWhere) { Ok "Java 17+ present: $javaWhere (the tool finds it automatically — no PATH change needed)" }
elseif ($Smoke) { Warn2 "Smoke mode: skipping Java offer" }
else {
    $ans = Read-Host "    Java 17+ not found. Download a portable copy now (no admin needed) for native .mpp support? [y/N]"
    if ($ans -match "^[Yy]") {
        try {
            $jdkRoot = Join-Path $env:LOCALAPPDATA "Programs\Microsoft"
            New-Item -ItemType Directory -Force -Path $jdkRoot | Out-Null
            $zip = Join-Path $InstallRoot "microsoft-jdk-17.zip"
            Write-Host "    Downloading Microsoft OpenJDK 17 (portable zip, ~180 MB — no admin prompt)..."
            Invoke-WebRequest -Uri "https://aka.ms/download-jdk/microsoft-jdk-17-windows-x64.zip" -OutFile $zip -UseBasicParsing
            Expand-Archive -Path $zip -DestinationPath $jdkRoot -Force
            Remove-Item -Force -ErrorAction SilentlyContinue $zip
            $javaWhere = Find-JavaNoAdmin
            if ($javaWhere) { Ok "Java 17 ready (no admin needed): $javaWhere" }
            else { Warn2 "Downloaded but java.exe not found where expected — .mpp stays available via MSPDI XML export" }
        } catch {
            Warn2 ("Java download failed (" + $_.Exception.Message + ") — skipped; .mpp files can still be opened after exporting to MSPDI XML from MS Project")
        }
    } else { Warn2 "Skipped — .mpp files can still be opened after exporting to MSPDI XML from MS Project" }
}

# --- 5. Ollama + this tier's local AI model -------------------------------------------
Step "Local AI ($OllamaModel) — the tool runs fully without it (skippable)"
if ($Smoke) { $ans = "n"; Warn2 "Smoke mode: skipping AI install" }
else { $ans = Read-Host "    Install Ollama + pull '$OllamaModel' (~$ModelDiskGB GB download)? [Y/n]" }
if ($ans -notmatch "^[Nn]") {
    if (-not (Get-Command ollama -ErrorAction SilentlyContinue)) {
        winget install --id Ollama.Ollama --exact --accept-source-agreements --accept-package-agreements --silent
        $env:Path = [Environment]::GetEnvironmentVariable("Path","Machine") + ";" + [Environment]::GetEnvironmentVariable("Path","User")
        Ok "Ollama installed"
    } else { Ok "Ollama already present" }
    Write-Host "    Pulling $OllamaModel (this is the big download — grab a coffee)..."
    & ollama pull $OllamaModel
    Ok "Model ready: $OllamaModel"
} else {
    Warn2 "Skipped — the tool works fully offline; AI prose features stay off until a model is installed"
}

# --- 6. Start / Stop commands, shortcuts, uninstaller, README -------------------------
Step "Creating Start/Stop shortcuts, uninstaller, and first-run README"
$startCmd = Join-Path $InstallRoot "Start-ScheduleForensics.cmd"
@"
@echo off
rem Schedule Forensics — start (loopback-only; the browser opens automatically).
start "" "$VenvDir\Scripts\pythonw.exe" -c "from schedule_forensics.launcher import main; main(port=$AppPort)"
"@ | Set-Content -Path $startCmd -Encoding ASCII

$stopCmd = Join-Path $InstallRoot "Stop-ScheduleForensics.cmd"
@"
@echo off
rem Schedule Forensics — graceful stop (asks the local app to shut itself down).
powershell -NoProfile -Command "try { Invoke-RestMethod -Method Post -Uri http://127.0.0.1:$AppPort/api/shutdown -TimeoutSec 5 | Out-Null; Write-Host 'Schedule Forensics stopped.' } catch { Write-Host 'Not running (or already stopped).' }"
timeout /t 3 >nul
"@ | Set-Content -Path $stopCmd -Encoding ASCII

$uninst = Join-Path $InstallRoot "Uninstall-ScheduleForensics.ps1"
@"
# Uninstall Schedule Forensics: removes the app folder + shortcuts.
# Leaves Python / Java / Ollama in place (shared programs); remove the AI model with:
#     ollama rm $OllamaModel
& "$stopCmd" | Out-Null
`$desk = [Environment]::GetFolderPath('Desktop')
`$menu = Join-Path ([Environment]::GetFolderPath('StartMenu')) 'Programs\Schedule Forensics'
Remove-Item -Force -ErrorAction SilentlyContinue (Join-Path `$desk 'Schedule Forensics.lnk')
Remove-Item -Force -ErrorAction SilentlyContinue (Join-Path `$desk 'Start Schedule Forensics.lnk')
Remove-Item -Force -ErrorAction SilentlyContinue (Join-Path `$desk 'Stop Schedule Forensics.lnk')
Remove-Item -Recurse -Force -ErrorAction SilentlyContinue `$menu
Remove-Item -Recurse -Force '$InstallRoot'
Write-Host 'Schedule Forensics removed.'
"@ | Set-Content -Path $uninst -Encoding UTF8

@"
Schedule Forensics — first run
==============================
Start:  double-click "Schedule Forensics" on the Desktop (the ONE icon).
        Your browser opens http://127.0.0.1:$AppPort — everything runs ON THIS MACHINE.
Stop:   just close the browser window / click Quit in the app — the server AND the
        local AI shut themselves down. (Stop-ScheduleForensics.cmd in $InstallRoot
        exists only as a troubleshooting fallback.)
Data:   your schedule files NEVER leave this computer (loopback-only by design).
AI:     enable it in AI Settings inside the app (model installed: $OllamaModel).
.mpp:   opening native .mpp needs Java 17+ (the installer offered it); otherwise
        save-as MSPDI XML in MS Project and open that.
Remove: run Uninstall-ScheduleForensics.ps1 in $InstallRoot.
"@ | Set-Content -Path (Join-Path $InstallRoot "FIRST-RUN-README.txt") -Encoding UTF8

if ($Smoke) {
    Ok "Smoke mode: launchers written to $InstallRoot only (no shortcuts)"
    Stop-Transcript | Out-Null
    Write-Host "SMOKE INSTALL OK" -ForegroundColor Green
    return
}
$shell = New-Object -ComObject WScript.Shell
$desk  = [Environment]::GetFolderPath("Desktop")
$menu  = Join-Path ([Environment]::GetFolderPath("StartMenu")) "Programs\Schedule Forensics"
New-Item -ItemType Directory -Force -Path $menu | Out-Null
# ONE icon (operator, ADR-0193): the app stops ITSELF (and the local AI it manages) when
# the browser window closes or Quit is clicked — a separate Stop icon only confused things.
# The prior installs' Start/Stop desktop icons are removed; Stop-ScheduleForensics.cmd
# stays in the install folder as a troubleshooting fallback.
foreach ($old in @("Start Schedule Forensics.lnk", "Stop Schedule Forensics.lnk")) {
    Remove-Item -Force -ErrorAction SilentlyContinue (Join-Path $desk $old)
    Remove-Item -Force -ErrorAction SilentlyContinue (Join-Path $menu $old)
}
foreach ($dir in @($desk, $menu)) {
    $lnk = $shell.CreateShortcut((Join-Path $dir "Schedule Forensics.lnk"))
    $lnk.TargetPath = (Join-Path $VenvDir "Scripts\pythonw.exe")
    $lnk.Arguments  = "-c `"from schedule_forensics.launcher import main; main(port=$AppPort)`""
    $lnk.WorkingDirectory = $InstallRoot
    $lnk.Description = "Schedule Forensics — starts the tool; closing the browser stops everything"
    $lnk.Save()
}
$unlnk = $shell.CreateShortcut((Join-Path $menu "Uninstall Schedule Forensics.lnk"))
$unlnk.TargetPath = "powershell.exe"
$unlnk.Arguments  = "-ExecutionPolicy Bypass -File `"$uninst`""
$unlnk.Save()
Ok "ONE desktop icon: 'Schedule Forensics' (self-stopping); uninstaller + README in $InstallRoot"

Stop-Transcript | Out-Null
Write-Host ""
Write-Host "DONE — double-click 'Schedule Forensics' on the Desktop to begin." -ForegroundColor Green
Write-Host "(Close the browser window to stop everything, including the local AI.)"
Write-Host "(Everything runs locally at http://127.0.0.1:$AppPort — no data leaves this machine.)"
}

